function seasonBatting(runs: number, balls: number, avg: number, srate: number, fifties: number, hundreds: number, hs: number, boundaryPct: number) {
  return {
    matches: Math.max(8, Math.round(balls / 18)),
    innings: Math.max(8, Math.round(balls / 20)),
    runs,
    balls,
    strikeRate: srate,
    average: avg,
    fifties,
    hundreds,
    highestScore: hs,
    boundaryPct,
  }
}

function seasonBowling(wkts: number, balls: number, runsConceded: number, economy: number, avg: number, bestFigures: string) {
  return {
    matches: Math.max(8, Math.round(balls / 18)),
    wickets: wkts,
    balls,
    runsConceded,
    economy,
    average: avg,
    bestFigures,
  }
}

function buildSeasons(
  battingSeries: Array<ReturnType<typeof seasonBatting> | undefined>,
  bowlingSeries: Array<ReturnType<typeof seasonBowling> | undefined>,
  startSeason = 2016,
) {
  return Array.from({ length: 10 }, (_, index) => {
    const season = startSeason + index
    return {
      season,
      played: Boolean(battingSeries[index] || bowlingSeries[index]),
      batting: battingSeries[index],
      bowling: bowlingSeries[index],
    }
  })
}

const seasons = (batting: Array<ReturnType<typeof seasonBatting> | undefined>, bowling: Array<ReturnType<typeof seasonBowling> | undefined>) =>
  buildSeasons(batting, bowling)

